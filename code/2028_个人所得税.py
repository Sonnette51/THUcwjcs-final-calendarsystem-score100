income = int(input())
threshold = 3500
TS = income - threshold
if TS <= 0:
    tax = 0
elif TS <= 500:
    tax = TS * 0.05
elif TS <= 2000:
    tax = 500 * 0.05 + (TS - 500) * 0.10
elif TS <= 5000:
    tax = 500 * 0.05 + 1500 * 0.10 + (TS - 2000) * 0.15
elif TS <= 20000:
    tax = 500 * 0.05 + 1500 * 0.10 + 3000 * 0.15 + (TS - 5000) * 0.20
elif TS <= 40000:
    tax = 500 * 0.05 + 1500 * 0.10 + 3000 * 0.15 + 15000 * 0.20 + (TS - 20000) * 0.25
elif TS <= 60000:
    tax = 500 * 0.05 + 1500 * 0.10 + 3000 * 0.15 + 15000 * 0.20 + 20000 * 0.25 + (TS - 40000) * 0.30
else:
    tax = 500 * 0.05 + 1500 * 0.10 + 3000 * 0.15 + 15000 * 0.20 + 20000 * 0.25 + 20000 * 0.30 + (TS - 60000) * 0.50
print("{:.1f}".format(tax))

